        function showCustomModal(title, msg, buttonsConfig) {
            modalTitle.innerText = title;
            modalMessage.innerText = msg;
            modalActionsContainer.innerHTML = "";
            
            buttonsConfig.forEach(btnConfig => {
                const btn = document.createElement("button");
                btn.innerText = btnConfig.text;
                if(btnConfig.className) btn.classList.add(btnConfig.className);
                btn.onclick = () => {
                    customModal.classList.add("hidden");
                    if(btnConfig.callback) btnConfig.callback();
                };
                modalActionsContainer.appendChild(btn);
            });
            customModal.classList.remove("hidden");
        }

        categoryButtons.forEach(button => {
            button.addEventListener("click", () => {
                initAudio();
                launchCategory(button.getAttribute("data-cat"));
            });
        });

        function handleBackRequest() {
            showCustomModal("Exit Match?", "Are you sure you want to return to the Main Menu? Your progress in this set will be wiped.", [
                { text: "Keep Playing", className: "modal-btn-cancel", callback: null },
                { text: "Quit Match", className: "modal-btn-confirm", callback: exitToMenu }
            ]);
        }

        backBtn.addEventListener("click", handleBackRequest);
        mobileBackBtn.addEventListener("click", handleBackRequest);
        clearBtn.addEventListener("click", ResetWorkingGuess);
        submitBtn.addEventListener("click", validateUserGuess);
        restartBtn.addEventListener("click", exitToMenu);

        function updateHUDDisplays() {
            sideScore.innerText = accumulatedScore;
            mobileScore.innerText = `Score: ${accumulatedScore}`;
            sideLevel.innerText = gameLevel;
            mobileLevel.innerText = `Lv: ${gameLevel}`;
        }

        function launchCategory(catKey) {
            currentCategory = catKey;
            accumulatedScore = 0;
            gameLevel = 1;
            currentLives = 3;
            
            let rawData = [...puzzleDatabase[currentCategory]];
            questionQueue = rawData.sort(() => Math.random() - 0.5);
            queueIndex = 0;

            updateHUDDisplays();
            livesCountDisplay.innerText = currentLives;
            
            menuScreen.classList.add("hidden");
            winScreen.classList.add("hidden");
            gameScreen.classList.remove("hidden");
            
            sidebarDesc.classList.add("hidden");
            gameSidebarHud.classList.remove("hidden");
            backBtn.classList.remove("hidden");
            
            buildLevelRules();
            buildPuzzleInstance();
        }

        function buildLevelRules() {
            if (gameLevel === 1) timerDuration = 45;
            else if (gameLevel === 2) timerDuration = 30;
            else timerDuration = 20;
        }

        function buildPuzzleInstance() {
            if(timerInterval) clearInterval(timerInterval);
            ResetWorkingGuess();
            
            let roundData = questionQueue[queueIndex];
            currentActiveWord = roundData.word;
            
            if (gameLevel === 3) {
                categoryNameDisplay.innerText = "??? (Hints locked at Level 3!)";
            } else {
                categoryNameDisplay.innerText = roundData.hint;
            }

            let wordLetters = currentActiveWord.split("");
            let safety = 0;
            do {
                wordLetters.sort(() => Math.random() - 0.5);
                safety++;
            } while (wordLetters.join("") === currentActiveWord && safety < 30);

            jumbledRow.innerHTML = "";
            wordLetters.forEach((char, idx) => {
                const tile = document.createElement("div");
                tile.className = "letter-tile";
                tile.innerText = char;
                tile.onclick = () => selectLetterTile(tile, char, idx);
                jumbledRow.appendChild(tile);
            });

            let timeLeft = timerDuration;
            timerCountDisplay.innerText = timeLeft;
            
            timerInterval = setInterval(() => {
                timeLeft--;
                timerCountDisplay.innerText = timeLeft;
                if (timeLeft <= 0) {
                    clearInterval(timerInterval);
                    processFailure(true);
                }
            }, 1000);
        }

        function selectLetterTile(tileElement, character, originalIndex) {
            if (tileElement.classList.contains("used")) return;
            playTileClick();
            tileElement.classList.add("used");
            userGuessArray.push({ char: character, origin: originalIndex, elem: tileElement });
            renderGuessRow();
        }

        function renderGuessRow() {
            guessRow.innerHTML = "";
            userGuessArray.forEach((item, index) => {
                const tile = document.createElement("div");
                tile.className = "letter-tile";
                tile.innerText = item.char;
                tile.onclick = () => removeLetterFromGuess(index);
                guessRow.appendChild(tile);
            });
        }

        function removeLetterFromGuess(index) {
            playTileClick();
            let removedItem = userGuessArray.splice(index, 1)[0];
            removedItem.elem.classList.remove("used");
            renderGuessRow();
        }

        function ResetWorkingGuess() {
            userGuessArray = [];
            guessRow.innerHTML = "";
            document.querySelectorAll("#jumbled-row .letter-tile").forEach(tile => tile.classList.remove("used"));
        }

        function validateUserGuess() {
            let constructedString = userGuessArray.map(item => item.char).join("");
            
            if (constructedString === currentActiveWord) {
                clearInterval(timerInterval);
                playCorrectSound();
                
                accumulatedScore += (10 * gameLevel);
                updateHUDDisplays();
                queueIndex++;

                if (queueIndex === 3 && gameLevel === 1) {
                    gameLevel = 2;
                    buildLevelRules();
                    showCustomModal("🚀 Level Up!", "Entering Level 2! The timer is running faster now.", [
                        { text: "Bring It On!", className: "modal-btn-confirm", callback: buildPuzzleInstance }
                    ]);
                    updateHUDDisplays();
                    return;
                } else if (queueIndex === 6 && gameLevel === 2) {
                    gameLevel = 3;
                    buildLevelRules();
                    showCustomModal("🔥 Level Up!", "Entering Level 3! No more hints and extreme speed mode active!", [
                        { text: "I'm Ready!", className: "modal-btn-confirm", callback: buildPuzzleInstance }
                    ]);
                    updateHUDDisplays();
                    return;
                }
                
                if (queueIndex < questionQueue.length) {
                    setTimeout(buildPuzzleInstance, 600);
                } else {
                    setTimeout(() => triggerGameEnd(true), 600);
                }
            } else {
                processFailure(false);
            }
        }

        function processFailure(isTimeout) {
            playFailSound();
            currentLives--;
            livesCountDisplay.innerText = currentLives;
            
            if (currentLives <= 0) {
                clearInterval(timerInterval);
                playGameOverSound();
                triggerGameEnd(false);
            } else {
                const noticeTitle = isTimeout ? "⏱️ Out of Time!" : "❌ Incorrect Guess";
                const noticeText = isTimeout ? "You ran out of seconds. You lost 1 life line." : "That structure doesn't match the hidden word. Try rearranging!";
                
                showCustomModal(noticeTitle, noticeText, [
                    { text: "Try Again", className: "modal-btn-confirm", callback: () => {
                        ResetWorkingGuess();
                        if (isTimeout) buildPuzzleInstance();
                    }}
                ]);
            }
        }

        function triggerGameEnd(isWin) {
            if(timerInterval) clearInterval(timerInterval);
            gameScreen.classList.add("hidden");
            winScreen.classList.remove("hidden");
            
            if (isWin) {
                winTitle.innerText = "🎉 Arena Mastered!";
                document.getElementById("final-stats").innerText = `Incredible run! You completed all active configurations.\nFinal Score: ${accumulatedScore}`;
            } else {
                winTitle.innerText = "💀 Defeat";
                document.getElementById("final-stats").innerText = `You ran out of lives. Keep practicing!\nFinal Score achieved: ${accumulatedScore}`;
            }

            if (typeof ytgame !== 'undefined') {
                ytgame.engagement.sendScore({ value: accumulatedScore });
            }
        }

        function exitToMenu() {
            if(timerInterval) clearInterval(timerInterval);
            winScreen.classList.add("hidden");
            gameScreen.classList.add("hidden");
            menuScreen.classList.remove("hidden");
            
            gameSidebarHud.classList.add("hidden");
            backBtn.classList.add("hidden");
            sidebarDesc.classList.remove("hidden");
        }