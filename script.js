        function initAudio() {
            if (!audioCtx) {
                audioCtx = new (window.AudioContext || window.webkitAudioContext)();
            }
            if (audioCtx && audioCtx.state === 'suspended') {
                audioCtx.resume();
            }
        }

        function playBeep(frequency, type, length) {
            try {
                if (!audioCtx) return;
                let osc = audioCtx.createOscillator();
                let gain = audioCtx.createGain();
                osc.type = type;
                osc.frequency.setValueAtTime(frequency, audioCtx.currentTime);
                gain.gain.setValueAtTime(0.15, audioCtx.currentTime);
                gain.gain.exponentialRampToValueAtTime(0.001, audioCtx.currentTime + length);
                osc.connect(gain);
                gain.connect(audioCtx.destination);
                osc.start();
                osc.stop(audioCtx.currentTime + length);
            } catch (e) {
                console.log("Audio routing error: ", e);
            }
        }

        const playTileClick = () => playBeep(587.33, "triangle", 0.08);
        const playCorrectSound = () => { 
            playBeep(523.25, "sine", 0.1);
            setTimeout(() => playBeep(659.25, "sine", 0.2), 90);
        };
        const playFailSound = () => { playBeep(196.00, "sawtooth", 0.25); };
        const playGameOverSound = () => { playBeep(110.00, "sawtooth", 0.6); };

